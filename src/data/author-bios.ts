export function normalizeAuthor(author: string): string {
  const trimmed = author.replace(/\s+/g, " ").trim();
  return AUTHOR_ALIASES[trimmed] ?? trimmed;
}

export function formatAuthorBio(bio: AuthorBio): string {
  return [
    `Страна: ${bio.country}`,
    `Город рождения: ${bio.birthCity}`,
    `Годы жизни: ${bio.years}`,
    ...bio.paragraphs,
  ].join("\n\n");
}

export function getAuthorBio(author: string): string | null {
  const canonical = normalizeAuthor(author);
  const bio = AUTHOR_BIOS[canonical];
  return bio ? formatAuthorBio(bio) : null;
}

export function getAuthorsWithBio(): string[] {
  return Object.keys(AUTHOR_BIOS);
}
